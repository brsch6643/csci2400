int getByte(int x, int n) {
	//this is a way to used shifting to multiply something
	//multiply n by 8 so that a byte can be shifted to the end and the then keeping just
	//those bytes by &0xFF
	n = n<<3;
	x = x>>n;
	x = x&0xFF;
  return x;
}
/* 
 * copyLSB - set all bits of result to least significant bit of x
 *   Example: copyLSB(5) = 0xFFFFFFFF, copyLSB(6) = 0x00000000
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int copyLSB(int x) {
	//leaves the last bit
	x = x&0x1;
	//shifts the last bit to the most significan bit
	x = x<<31;
	//copies the sig bit to all the bits of the slots available
	x = x>>31;
	
	
  return x;
}
